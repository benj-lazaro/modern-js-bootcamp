// While Loops Demo

// Iterate numbers from 0 to 5 using a for loop
for (let index = 0; index <= 5; index++) {
    console.log(`For loop ${index}`);
}

// Iterate numbers from 0 to 5 using a while loop
let counter = 0;
while (counter <= 5) {
    console.log(`While loop ${counter}`);
    counter ++;
}
