// Function With Multiple Arguments

// Function with a single parameter
function square(num) {
    console.log(num * num);
}

// Function with multiple parameters
function sum(x, y) {
    console.log(x + y);
}

function divide(x, y) {
    console.log(x / y);
}

console.log("Calling square(2)");
square(2);

console.log("Calling sum(4, 5)");
sum(4, 5);

console.log("Calling divide(10, 2)")
divide(10, 2)
